import math

a = 3
b = 4
# Hypotenuse using Pythagoras theorem
hypotenuse = math.hypot(a, b)
print("Hypotenuse:", hypotenuse)

# Trigonometry
angle_deg = 30
angle_rad = math.radians(angle_deg)
print("Sin(30):", math.sin(angle_rad))
print("Cos(30):", math.cos(angle_rad))

# Logarithmic Functions
num = 100
print("Log base 10 of 100:", math.log10(num))
print("Natural log of e:", math.log(math.e))
